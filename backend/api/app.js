const express = require('express');
const cors = require('cors');
const helmet = require('helmet');
const morgan = require('morgan');
require('dotenv').config();

const app = express();
const PORT = process.env.PORT || 3001;

// Middleware
app.use(helmet());
app.use(cors());
app.use(morgan('combined'));
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

// Routes
app.get('/', (req, res) => {
    res.json({
        message: 'StratIQ API Server is running!',
        version: '1.0.0',
        status: 'healthy'
    });
});

// Health check endpoint
app.get('/health', (req, res) => {
    res.json({
        status: 'OK',
        timestamp: new Date().toISOString(),
        uptime: process.uptime()
    });
});

// API routes (to be implemented)
app.use('/api/auth', (req, res) => res.json({ message: 'Auth endpoints coming soon' }));
app.use('/api/trading', (req, res) => res.json({ message: 'Trading endpoints coming soon' }));
app.use('/api/recall', (req, res) => res.json({ message: 'Recall integration coming soon' }));
app.use('/api/vincent', (req, res) => res.json({ message: 'Vincent integration coming soon' }));

// Error handling
app.use((err, req, res, next) => {
    console.error(err.stack);
    res.status(500).json({
        error: 'Something went wrong!',
        message: process.env.NODE_ENV === 'development' ? err.message : 'Internal server error'
    });
});

// 404 handler
app.use((req, res) => {
    res.status(404).json({ error: 'Endpoint not found' });
});

app.listen(PORT, () => {
    console.log(`ðŸš€ StratIQ API Server running on port ${PORT}`);
    console.log(`ðŸ“Š Environment: ${process.env.NODE_ENV || 'development'}`);
    console.log(`ðŸ”— Health check: http://localhost:${PORT}/health`);
});

module.exports = app;